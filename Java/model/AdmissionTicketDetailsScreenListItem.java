package model;

import java.sql.Date;

public class AdmissionTicketDetailsScreenListItem {
    private String firstName, lastName, gender, admissionText;
    private Date birthDate;

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAdmissionText() {
        return admissionText;
    }

    public void setAdmissionText(String admissionText) {
        this.admissionText = admissionText;
    }

    public Date getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(Date birthDate) {
        this.birthDate = birthDate;
    }
}
