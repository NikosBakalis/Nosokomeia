package model;

public class InitialDoctorScreenListItem {
    private String amka, firstName, lastName, hostClinic, patientChamber;

    public String getAmka() {
        return amka;
    }

    public void setAmka(String amka) {
        this.amka = amka;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getHostClinic() {
        return hostClinic;
    }

    public void setHostClinic(String hostClinic) {
        this.hostClinic = hostClinic;
    }

    public String getPatientChamber() {
        return patientChamber;
    }

    public void setPatientChamber(String chamber) {
        this.patientChamber = chamber;
    }
}
